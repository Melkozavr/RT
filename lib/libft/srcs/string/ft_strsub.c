#include "ftstring.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char *res;

	if (s == NULL || !(res = ft_strnew(len)))
		return (NULL);
	s += start;
	return (ft_strncpy(res, s, len));
}
