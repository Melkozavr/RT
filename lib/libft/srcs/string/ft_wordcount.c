#include "ftstring.h"

size_t	ft_wordcount(char const *str, char c)
{
	size_t count;

	count = 0;
	while (*str)
	{
		while (*str && *str == c)
			str++;
		if (*str)
		{
			count++;
			while (*str && *str != c)
				str++;
		}
	}
	return (count);
}
